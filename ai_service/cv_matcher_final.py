# ── 3A: TEXT NORMALIZATION ───────────────────────────────────

def normalize_text(text: str, aggressive: bool = False) -> str:
    """
    Normalisasi teks untuk preprocessing.

    Args:
        text       : input teks
        aggressive : True  → hapus special chars (untuk keyword extraction)
                     False → pertahankan tanda baca (untuk SBERT)
    Returns:
        str: teks yang sudah dinormalisasi
    """
    if not text:
        return ""

    text = text.replace("\n", " ").replace("\r", " ")

    if aggressive:
        text = re.sub(r"[^a-zA-Z0-9\s\+\#\.\/\-]", " ", text)
    else:
        text = re.sub(r"[^\w\s\.\,\-\(\)\+\#\/]", " ", text)

    return re.sub(r"\s+", " ", text).strip()


def clean_text_for_sbert(text: str) -> str:
    """Gentle normalization — pertahankan makna semantik untuk SBERT."""
    return normalize_text(text, aggressive=False)


# ── 3B: SECTION EXTRACTION ───────────────────────────────────

def is_heading_line(line: str, section_keywords: Dict) -> Tuple[bool, Optional[str]]:
    """
    Deteksi apakah sebuah baris adalah heading section CV.

    Format yang didukung:
        - Uppercase  : "PROFESSIONAL SUMMARY"
        - Lowercase  : "professional summary"
        - Title Case : "Professional Summary"
        - Bernomor   : "1. Education" atau "2) Work Experience"
        - Berkolon   : "Skills:" atau "Experience:"

    Diabaikan (bukan heading):
        - Bullet point : "- Python" atau "• Developed..."
        - Teks panjang : > 80 karakter
        - Terlalu banyak kata : > 6 kata
        - Tidak cocok keyword manapun

    Args:
        line            : satu baris teks dari CV
        section_keywords: SECTION_KEYWORDS dict

    Returns:
        Tuple (is_heading: bool, matched_section: str | None)
    """
    line = line.strip()

    if not line or len(line) < 3:
        return False, None

    if line[0] in ('-', '•', '*'):
        return False, None

    if len(line) > 80:
        return False, None

    # Normalisasi: hapus trailing colon & numbering
    line_clean = re.sub(r'^(\d+[\.\)])\s+', '', line.rstrip(':')).strip()

    if len(line_clean.split()) > 6:
        return False, None

    line_lower = line_clean.lower()

    for section_name, keywords in section_keywords.items():
        all_keywords = keywords['en'] + keywords['id']
        for kw in sorted(all_keywords, key=len, reverse=True):
            if re.search(r'\b' + re.escape(kw) + r'\b', line_lower):
                return True, section_name

    return False, None


def extract_cv_sections(cv_text: str) -> Dict[str, str]:
    """
    Ekstrak section CV berdasarkan deteksi heading per baris.

    Algoritma:
        1. Split teks jadi baris
        2. Tiap baris dicek: heading atau konten?
        3. Konten dikumpulkan ke section yang aktif
        4. Heading baru = simpan section lama, mulai section baru
        5. Setiap section di-clean untuk SBERT

    Args:
        cv_text: full teks CV

    Returns:
        Dict[section_name: cleaned_section_text]
        Section kosong → string kosong ""
    """
    sections = {s: "" for s in CONFIG['sections']}
    current_section = None
    buffer = []

    for line in cv_text.split('\n'):
        is_heading, matched = is_heading_line(line, SECTION_KEYWORDS)

        if is_heading:
            if current_section is not None:
                sections[current_section] = '\n'.join(buffer).strip()
            current_section = matched
            buffer = []
        else:
            if current_section is not None:
                buffer.append(line)

    # Simpan section terakhir
    if current_section is not None:
        sections[current_section] = '\n'.join(buffer).strip()

    # Clean tiap section untuk SBERT
    return {k: clean_text_for_sbert(v) for k, v in sections.items()}


