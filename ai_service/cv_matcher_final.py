# ── 6A: MATCH LABEL & REASONING ──────────────────────────────

def get_match_label(score: float) -> Tuple[str, str]:
    """
    Konversi score numerik ke label match.

    Returns:
        Tuple (label_text, emoji)
        Contoh: ('Strong Match', '🟢')
    """
    if score >= MATCH_THRESHOLDS['strong']:
        return MATCH_LABELS['strong']
    elif score >= MATCH_THRESHOLDS['potential']:
        return MATCH_LABELS['potential']
    return MATCH_LABELS['low']


def generate_reasoning(score_result: Dict) -> str:
    """
    Generate reasoning singkat (1–2 kalimat) untuk HRD.

    Logika:
        - Identifikasi section terkuat & terlemah
        - Sesuaikan kalimat dengan label match
        - Bantu HRD memahami dasar penilaian AI

    Args:
        score_result: output dari score_cv()

    Returns:
        str: reasoning natural dalam Bahasa Indonesia
    """
    section_scores = score_result.get('section_scores', {})
    weights        = score_result.get('weights_used', SECTION_WEIGHTS_BASE)
    final_score    = score_result.get('final_score', 0)

    active = {
        k: v for k, v in section_scores.items()
        if weights.get(k, 0) > 0 and v > 0
    }

    if not active:
        return "Tidak cukup konten CV untuk dianalisis."

    strongest       = max(active, key=active.get)
    weakest         = min(active, key=active.get)
    strongest_score = active[strongest]
    weakest_score   = active[weakest]

    SECTION_ID = {
        'experience': 'pengalaman kerja',   'skills': 'keahlian teknis',
        'summary':    'profil kandidat',    'education': 'latar belakang pendidikan',
        'certifications': 'sertifikasi',
    }

    s_label = SECTION_ID.get(strongest, strongest)
    w_label = SECTION_ID.get(weakest, weakest)

    if final_score >= MATCH_THRESHOLDS['strong']:
        reasoning = f"Kandidat menunjukkan relevansi tinggi pada bagian {s_label} ({strongest_score:.0f}%)."
        if weakest_score < 35 and weakest != strongest:
            reasoning += f" Bagian {w_label} relatif kurang relevan ({weakest_score:.0f}%) — perlu eksplorasi saat interview."
    elif final_score >= MATCH_THRESHOLDS['potential']:
        reasoning = (
            f"Kandidat memiliki potensi di bagian {s_label} ({strongest_score:.0f}%), "
            f"namun bagian {w_label} perlu digali lebih lanjut saat interview."
        )
    else:
        reasoning = "Profil kandidat kurang sesuai dengan kebutuhan posisi ini."
        if strongest_score > 30:
            reasoning += f" Relevansi hanya terdeteksi pada bagian {s_label} ({strongest_score:.0f}%)."

    return reasoning


# ── 6B: LAPORAN TEKS ─────────────────────────────────────────

def build_report(
    ranked_results:     List[Dict],
    jd_title:           str  = "Posisi yang Dilamar",
    top_n:              Optional[int] = None,
    show_section_detail: bool = False,
) -> str:
    """
    Format hasil ranking menjadi laporan teks yang HRD-friendly.

    Args:
        ranked_results      : output dari rank_cvs()
        jd_title            : nama posisi untuk header laporan
        top_n               : tampilkan N kandidat teratas (None = semua)
        show_section_detail : tampilkan per-section score

    Returns:
        str: laporan ranking siap cetak / kirim ke frontend
    """
    to_show = ranked_results[:top_n] if top_n else ranked_results
    total   = len(ranked_results)

    strong    = sum(1 for r in ranked_results if r['final_score'] >= MATCH_THRESHOLDS['strong'])
    potential = sum(1 for r in ranked_results
                    if MATCH_THRESHOLDS['potential'] <= r['final_score'] < MATCH_THRESHOLDS['strong'])
    low       = total - strong - potential

    SEP  = "=" * 68
    SEP2 = "-" * 68

    lines = [
        SEP,
        f"  LAPORAN RANKING CV — {jd_title.upper()}",
        f"  {datetime.now().strftime('%d %B %Y, %H:%M')}",
        SEP,
        f"  Total kandidat   : {total}",
        f"  🟢 Strong Match   : {strong}",
        f"  🟡 Potential Match: {potential}",
        f"  🔴 Low Match      : {low}",
    ]

    if top_n and top_n < total:
        lines.append(f"\n  Menampilkan {top_n} dari {total} kandidat:")

    lines.append(SEP2)

    for r in to_show:
        label, emoji = get_match_label(r['final_score'])
        lines += [
            f"\n  Rank #{r['rank']}  {emoji} {label}",
            f"  ID       : {r.get('id', f'Kandidat #{r["rank"]}')}",
        ]
        if r.get('category'):
            lines.append(f"  Kategori : {r['category']}")
        lines.append(f"  Score    : {r['final_score']:.1f}%")
        lines.append(f"  Analisis : {generate_reasoning(r)}")

        if show_section_detail:
            weights = r.get('weights_used', {})
            active  = {k: v for k, v in r['section_scores'].items()
                       if weights.get(k, 0) > 0 and v > 0}
            if active:
                lines.append("  Detail   :")
                for sec, sc in sorted(active.items(), key=lambda x: x[1], reverse=True):
                    w   = weights.get(sec, 0)
                    bar = '█' * int(sc / 10)
                    lines.append(f"    {sec:<15} {sc:>5.1f}%  {bar}  (bobot {w:.0%})")

        lines.append("  " + SEP2)

    lines += [
        "\n  ⚠️  DISCLAIMER",
        "  Score adalah indikator relevansi semantik berbasis AI.",
        "  Sistem ini adalah alat bantu penyaringan — bukan pengambil keputusan.",
        "  Keputusan rekrutmen final sepenuhnya ada di tangan HRD.",
        SEP,
    ]

    return '\n'.join(lines)


