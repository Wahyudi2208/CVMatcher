def encode_text(text: str) -> Optional[np.ndarray]:
    """
    Encode satu teks ke vector SBERT (384 dimensi).

    Args:
        text: teks yang akan di-encode

    Returns:
        np.ndarray shape (384,), atau None jika teks terlalu pendek
    """
    if not text or len(text.strip()) < 5:
        return None
    return _sbert_model.encode(clean_text_for_sbert(text), convert_to_numpy=True)


def encode_sections(cv_sections: Dict[str, str]) -> Dict[str, Optional[np.ndarray]]:
    """
    Encode semua section CV ke vector SBERT.

    Args:
        cv_sections: Dict hasil extract_cv_sections()

    Returns:
        Dict[section_name: embedding | None jika section kosong]
    """
    return {
        name: encode_text(text) if text and len(text.strip()) > 5 else None
        for name, text in cv_sections.items()
    }


def encode_jd(jd_text: str) -> np.ndarray:
    """
    Encode full teks JD ke satu vector SBERT.

    JD di-encode sebagai satu unit karena strukturnya tidak
    sekonsisten CV — lebih aman encode keseluruhan JD.

    Args:
        jd_text: full teks job description

    Returns:
        np.ndarray shape (384,)
    """
    return _sbert_model.encode(clean_text_for_sbert(jd_text), convert_to_numpy=True)


def section_similarity(
    cv_embedding: Optional[np.ndarray],
    jd_embedding: np.ndarray
) -> float:
    """
    Hitung cosine similarity antara satu section CV dengan JD.

    Args:
        cv_embedding : embedding section CV (None jika section kosong)
        jd_embedding : embedding JD

    Returns:
        float: similarity 0.0–1.0
               0.0 jika section CV kosong
    """
    if cv_embedding is None:
        return 0.0
    sim = cosine_similarity(
        cv_embedding.reshape(1, -1),
        jd_embedding.reshape(1, -1)
    )[0][0]
    return float(np.clip(sim, 0.0, 1.0))


# ── 4B: DYNAMIC WEIGHTING ────────────────────────────────────

def _detect_jd_signals(jd_text: str) -> Dict[str, float]:
    """
    Deteksi sinyal eksplisit per section dari teks JD.

    Setiap section punya set sinyal (kata/frasa kunci) yang
    menandakan section tersebut diprioritaskan di JD ini.

    Returns:
        Dict[section_name: signal_strength 0.0–1.0]
    """
    t = jd_text.lower()

    signals = {
        'experience': min(1.0, sum([
            bool(re.search(
                r'\d+\+?\s*(?:years?|yrs?|tahun)\s+(?:of\s+)?(?:experience|pengalaman)', t)),
            bool(re.search(
                r'(?:minimum|at least|minimal|setidaknya)\s+\d+\s*(?:years?|tahun)', t)),
            any(w in t for w in ['work experience', 'professional experience',
                                  'pengalaman kerja', 'track record', 'experienced in']),
            any(w in t for w in ['experience required', 'experienced candidate']),
        ]) * 0.35),

        'skills': min(1.0, sum([
            any(w in t for w in ['skills', 'proficiency', 'proficient', 'expertise',
                                  'keahlian', 'kemampuan', 'kompetensi']),
            any(w in t for w in ['required skills', 'technical skills', 'must have']),
            any(w in t for w in ['familiar with', 'knowledge of', 'experience with']),
            bool(re.search(r'\b(?:python|java|sql|aws|excel|sap|javascript)\b', t)),
        ]) * 0.28),

        'education': min(1.0, sum([
            any(w in t for w in ['bachelor', 'master', 'phd', 'degree', 'diploma',
                                  'sarjana', 'magister', 's1', 's2']),
            any(w in t for w in ['education required', 'educational background',
                                  'latar belakang pendidikan']),
            any(w in t for w in ['computer science', 'engineering', 'accounting',
                                  'teknik informatika', 'statistics', 'mathematics']),
            bool(re.search(r'(?:degree|diploma)\s+in\b', t)),
        ]) * 0.27),

        'summary': min(1.0, 0.3 + sum([
            any(w in t for w in ['leadership', 'communication', 'teamwork',
                                  'analytical', 'problem solving', 'kepemimpinan']),
            any(w in t for w in ['passionate', 'motivated', 'proactive', 'fast learner']),
            any(w in t for w in ['strong background', 'proven track', 'demonstrated ability']),
        ]) * 0.15),

        'certifications': min(1.0, sum([
            any(w in t for w in ['certified', 'certification', 'certificate',
                                  'sertifikat', 'sertifikasi', 'lisensi']),
            any(w in t for w in ['cpa', 'aws certified', 'pmp', 'cissp', 'cfa']),
            bool(re.search(r'(?:certification|certificate)\s+(?:required|preferred|is a plus)', t)),
        ]) * 0.35),

        'projects':     0.0,
        'achievements': 0.0,
    }

    return signals


def compute_dynamic_weights(jd_text: str, verbose: bool = False) -> Dict[str, float]:
    """
    Hitung bobot section secara dinamis berdasarkan konten JD.

    Semakin eksplisit sebuah section disebutkan di JD,
    semakin tinggi bobotnya. Total bobot selalu = 1.0.

    Hard caps per section:
        experience    : 25% – 55%
        skills        : 20% – 45%
        summary       :  5% – 20%
        education     :  3% – 20%
        certifications:  0% – 15%
        projects      :  0%  (tidak di-score)
        achievements  :  0%  (tidak di-score)

    Args:
        jd_text : full teks JD
        verbose : print detail perhitungan weights

    Returns:
        Dict[section_name: weight] dengan total = 1.0
    """
    BASE = {
        'experience': 0.35, 'skills': 0.28, 'summary': 0.12,
        'education':  0.08, 'certifications': 0.02,
        'projects':   0.00, 'achievements':   0.00,
    }
    CAPS = {
        'experience':     (0.25, 0.55),
        'skills':         (0.20, 0.45),
        'summary':        (0.05, 0.20),
        'education':      (0.03, 0.20),
        'certifications': (0.00, 0.15),
        'projects':       (0.00, 0.00),
        'achievements':   (0.00, 0.00),
    }

    signals = _detect_jd_signals(jd_text)

    # Base + signal boost (max +30%)
    raw = {s: BASE[s] + signals.get(s, 0.0) * 0.3 for s in BASE}

    # Apply hard caps
    capped = {s: float(np.clip(raw[s], *CAPS[s])) for s in raw}

    # Normalize → total = 1.0
    total = sum(capped.values())
    weights = {s: v / total for s, v in capped.items()} if total > 0 else BASE.copy()

    if verbose:
        print("  ⚖️  Dynamic Weights:")
        for s, w in weights.items():
            if w > 0:
                print(f"     {s:<15} {w:.1%}  {'█' * int(w * 40)}")
        print(f"     {'TOTAL':<15} {sum(weights.values()):.1%}")

    return weights


