# ── 5A: HYBRID SCORER ────────────────────────────────────────

def score_cv(
    cv_text: str,
    jd_text: str,
    jd_embedding:     Optional[np.ndarray] = None,
    dynamic_weights:  Optional[Dict]        = None,
    hybrid_alpha:     float                 = 0.7,
) -> Dict:
    """
    Score satu CV terhadap satu JD menggunakan hybrid scoring.

    Formula:
        final = (section_weighted_score × alpha) + (fulltext_score × (1−alpha))

    Fallback:
        Jika CV flat (< 2 section terdeteksi) → fulltext 100%
        Ini memastikan CV tanpa heading tetap mendapat score yang fair.

    Args:
        cv_text         : full teks CV (sudah di-clean)
        jd_text         : full teks JD
        jd_embedding    : pre-computed JD embedding (opsional, efisiensi batch)
        dynamic_weights : pre-computed weights (opsional, efisiensi batch)
        hybrid_alpha    : bobot section score (default 0.7)

    Returns:
        Dict berisi:
            final_score     : float 0–100 (score akhir)
            section_score   : float 0–100 (komponen section-weighted)
            fulltext_score  : float 0–100 (komponen full-text)
            scoring_mode    : 'hybrid' | 'fulltext_fallback'
            section_scores  : Dict per-section score (0–100)
            weights_used    : Dict bobot yang digunakan
            non_empty_sections : int jumlah section yang terdeteksi
    """
    # Pastikan weights & embedding tersedia
    if dynamic_weights is None:
        dynamic_weights = compute_dynamic_weights(jd_text)
    if jd_embedding is None:
        jd_embedding = encode_jd(jd_text)

    # ── Komponen 1: Section-weighted score ──
    cv_sections   = extract_cv_sections(cv_text)
    cv_embeddings = encode_sections(cv_sections)

    section_scores  = {}
    weighted_total  = 0.0
    active_weight   = 0.0

    for sec, weight in dynamic_weights.items():
        if weight == 0.0:
            section_scores[sec] = 0.0
            continue

        sim = section_similarity(cv_embeddings.get(sec), jd_embedding)
        section_scores[sec] = round(sim * 100, 2)

        if cv_embeddings.get(sec) is not None:
            weighted_total += sim * weight
            active_weight  += weight

    section_score = (weighted_total / active_weight * 100) if active_weight > 0 else 0.0

    # ── Komponen 2: Full-text score ──
    fulltext_emb   = encode_text(clean_text_for_sbert(cv_text))
    fulltext_score = section_similarity(fulltext_emb, jd_embedding) * 100

    # ── Hybrid combination ──
    non_empty = sum(1 for v in cv_sections.values() if v and len(v) > 20)

    if non_empty >= 2:
        final = section_score * hybrid_alpha + fulltext_score * (1 - hybrid_alpha)
        mode  = 'hybrid'
    else:
        final = fulltext_score
        mode  = 'fulltext_fallback'

    return {
        'final_score':        round(float(np.clip(final, 0.0, 100.0)), 2),
        'section_score':      round(section_score, 2),
        'fulltext_score':     round(fulltext_score, 2),
        'scoring_mode':       mode,
        'section_scores':     section_scores,
        'section_texts':      cv_sections,
        'weights_used':       dynamic_weights,
        'non_empty_sections': non_empty,
    }


# ── 5B: BATCH RANKER ─────────────────────────────────────────

def rank_cvs(
    cv_list:       List[Dict],
    jd_text:       str,
    top_n:         Optional[int] = None,
    hybrid_alpha:  float         = 0.7,
    show_progress: bool          = True,
) -> List[Dict]:
    """
    Rank banyak CV terhadap satu JD.

    Efisiensi batch:
        - JD di-encode SEKALI
        - Dynamic weights dihitung SEKALI
        - Semua CV pakai embedding & weights yang sama

    Args:
        cv_list       : List of Dict, tiap item wajib punya key:
                            'id'   : identifier CV
                            'text' : teks CV
                        Key lain (category, name, dll) dipertahankan di output.
        jd_text       : full teks JD
        top_n         : ambil N teratas saja (None = semua)
        hybrid_alpha  : bobot section score (default 0.7)
        show_progress : tampilkan progress bar tqdm

    Returns:
        List of Dict sorted by final_score descending.
        Setiap item = data CV original + hasil scoring + 'rank'.
    """
    print("  📊 Analyzing JD...")
    weights      = compute_dynamic_weights(jd_text)
    jd_embedding = encode_jd(jd_text)

    results  = []
    iterator = tqdm(cv_list, desc="Scoring CVs") if show_progress else cv_list

    for cv_data in iterator:
        cv_text = cv_data.get('text', '')
        if not cv_text:
            continue

        scored = score_cv(
            cv_text, jd_text,
            jd_embedding    = jd_embedding,
            dynamic_weights = weights,
            hybrid_alpha    = hybrid_alpha,
        )
        results.append({**cv_data, **scored})

    results.sort(key=lambda x: x['final_score'], reverse=True)

    for i, r in enumerate(results):
        r['rank'] = i + 1

    return results[:top_n] if top_n else results


