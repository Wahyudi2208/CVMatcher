def analyze_single(cv_input: str, jd_text: str, jd_title: str = "Posisi") -> Dict:
    """
    Analisis satu CV terhadap satu JD.
    Digunakan untuk fitur Job Seeker (upload 1 CV).

    Args:
        cv_input : path PDF atau teks CV langsung
        jd_text  : full teks JD
        jd_title : nama posisi (untuk laporan)

    Returns:
        Dict berisi:
            score_result : output score_cv() lengkap
            label        : Tuple (label_text, emoji)
            reasoning    : str reasoning untuk kandidat
            report       : str laporan teks lengkap
    """
    cv_text      = load_cv(cv_input)
    score_result = score_cv(cv_text, jd_text)
    label        = get_match_label(score_result['final_score'])
    reasoning    = generate_reasoning(score_result)

    report = build_report(
        ranked_results=[{**score_result, 'id': 'Kandidat', 'rank': 1}],
        jd_title=jd_title,
        show_section_detail=True,
    )

    return {
        'score_result': score_result,
        'label':        label,
        'reasoning':    reasoning,
        'report':       report,
    }


def analyze_batch(
    cv_inputs:  List[Dict],
    jd_text:    str,
    jd_title:   str           = "Posisi",
    top_n:      Optional[int] = None,
    show_detail: bool         = False,
) -> Dict:
    """
    Ranking banyak CV terhadap satu JD.
    Digunakan untuk fitur HRD (upload multiple CV).

    Args:
        cv_inputs  : List of Dict dengan key:
                        'id'    : nama file / identifier
                        'text'  : teks CV (sudah di-load)
                     Atau langsung dari load_cv() per item.
        jd_text    : full teks JD
        jd_title   : nama posisi
        top_n      : tampilkan N teratas di laporan
        show_detail: tampilkan per-section score di laporan

    Returns:
        Dict berisi:
            ranked  : List hasil ranking lengkap
            report  : str laporan teks
            summary : Dict statistik (total, strong, potential, low)
    """
    ranked = rank_cvs(cv_inputs, jd_text, top_n=None)

    report = build_report(
        ranked_results=ranked,
        jd_title=jd_title,
        top_n=top_n,
        show_section_detail=show_detail,
    )

    total     = len(ranked)
    strong    = sum(1 for r in ranked if r['final_score'] >= MATCH_THRESHOLDS['strong'])
    potential = sum(1 for r in ranked
                    if MATCH_THRESHOLDS['potential'] <= r['final_score'] < MATCH_THRESHOLDS['strong'])

    return {
        'ranked':  ranked,
        'report':  report,
        'summary': {
            'total':     total,
            'strong':    strong,
            'potential': potential,
            'low':       total - strong - potential,
        },
    }


