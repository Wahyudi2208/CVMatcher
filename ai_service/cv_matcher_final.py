def _fix_encoding(text: str) -> str:
    """
    Perbaiki encoding error umum pada teks hasil ekstraksi PDF/CSV.

    Karakter yang diperbaiki:
        â¢  → •    (bullet point)
        â€™ → '    (apostrophe)
        â€" → –    (en dash)
        â€œ → "    (open quote)
        â€  → "    (close quote)
    """
    if not text:
        return ""

    try:
        text = text.encode('latin-1').decode('utf-8')
    except (UnicodeEncodeError, UnicodeDecodeError):
        pass

    fixes = {
        'â¢': '•', 'â€™': "'", 'â€˜': "'",
        'â€œ': '"', 'â€': '"',  'â€"': '–',
        'â€"': '—', 'Ã©': 'é',  'Ã¨': 'è',
        '\x00': '',  '\uf0b7': '•',
    }
    for wrong, correct in fixes.items():
        text = text.replace(wrong, correct)

    return text


def extract_text_from_pdf(pdf_path: str) -> str:
    """
    Ekstrak teks dari file PDF menggunakan pdfplumber.

    Args:
        pdf_path: path ke file PDF

    Returns:
        str: teks hasil ekstraksi, "" jika gagal atau file tidak ditemukan

    Catatan:
        PDF berbasis gambar (scan) tidak bisa diekstrak dengan metode ini.
        PDF 2 kolom mungkin menghasilkan urutan teks yang tidak sempurna.
    """
    path = Path(pdf_path)
    if not path.exists():
        print(f"  ⚠️  File tidak ditemukan: {pdf_path}")
        return ""

    try:
        pages = []
        with pdfplumber.open(path) as pdf:
            for i, page in enumerate(pdf.pages):
                text = page.extract_text()
                if text:
                    pages.append(text)
                else:
                    print(f"  ⚠️  Halaman {i+1} kosong (mungkin berbasis gambar)")

        if not pages:
            print(f"  ❌ Tidak ada teks yang berhasil diekstrak: {pdf_path}")
            return ""

        combined = '\n'.join(pages)
        combined = _fix_encoding(combined)
        combined = re.sub(r'\n{3,}', '\n\n', combined)
        combined = re.sub(r'[ \t]+', ' ', combined)
        return combined.strip()

    except Exception as e:
        print(f"  ❌ Error membaca PDF {pdf_path}: {e}")
        return ""


def extract_text_from_csv_row(resume_text: str) -> str:
    """
    Clean teks resume dari kolom dataset CSV (misal Kaggle Resume Dataset).

    Args:
        resume_text: teks mentah dari kolom 'Resume'

    Returns:
        str: teks yang sudah bersih
    """
    if not resume_text or not isinstance(resume_text, str):
        return ""

    text = _fix_encoding(resume_text)
    text = re.sub(r'\r\n|\r', '\n', text)
    text = re.sub(r'\n{3,}', '\n\n', text)
    text = re.sub(r'[ \t]+', ' ', text)
    return text.strip()


def load_cv(input_data: str, source: str = 'auto') -> str:
    """
    Universal CV loader — deteksi otomatis PDF vs teks biasa.

    Args:
        input_data : path ke PDF, atau teks CV langsung
        source     : 'pdf', 'text', atau 'auto' (default)

    Returns:
        str: teks CV yang sudah bersih dan siap diproses
    """
    if source == 'auto':
        source = 'pdf' if isinstance(input_data, str) and \
                          input_data.lower().endswith('.pdf') else 'text'

    return extract_text_from_pdf(input_data) if source == 'pdf' \
        else extract_text_from_csv_row(input_data)


